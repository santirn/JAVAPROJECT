    public Ganadora getGanadora() {
        ArrayList<Integer> los6numeros = new ArrayList<>();
        los6numeros.add(Integer.parseInt(tfNum1.getText()));
        los6numeros.add(Integer.parseInt(tfNum2.getText()));
        los6numeros.add(Integer.parseInt(tfNum3.getText()));
        los6numeros.add(Integer.parseInt(tfNum4.getText()));
        los6numeros.add(Integer.parseInt(tfNum5.getText()));
        los6numeros.add(Integer.parseInt(tfNum6.getText()));
        int complementario = Integer.parseInt(tfComplementario.getText());
        int reintegro = Integer.parseInt(tfReintegro.getText());
        return new Ganadora(los6numeros, complementario, reintegro);
    }

